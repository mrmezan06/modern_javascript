let age = 25;

// Loose comparison
console.log('Loose comparison');
console.log(age == 25);
console.log(age == '25');

console.log(age != 25);
console.log(age != '25');

// Strict comparison
console.log('Strict Comparison');
console.log(age === 25);
console.log(age === '25');

console.log(age !== 25);
console.log(age !== '25');